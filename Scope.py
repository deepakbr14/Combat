from Retro3D import *



###############################################################################
#
###############################################################################
class Scope:


    ###############################################################################
    #
    ###############################################################################
    def __init__(self, image:pg.image, pos_base:pg.math.Vector2):
    
        self.image = image
        rect = self.image.get_rect()
        self.pos_base = pg.Vector2(pos_base.x - rect.width / 2, pos_base.y - rect.height / 2)

        self.flash = 0

    ###############################################################################
    #
    ###############################################################################
    def shoot(self):
        self.flash = 10 

    ###############################################################################
    #
    ###############################################################################
    def update(self):
        
        if self.flash > 0:
            self.flash -= 1

    ###############################################################################
    #
    ###############################################################################
    def draw(self, screen):
    
        if self.flash > 0 and self.flash%2 == 1:
            pass
        else:
            screen.blit(self.image, self.pos_base)
